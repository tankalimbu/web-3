fun main() {
    val add = 6 + 2
    println(add) // 8

    val minus = 6 - 2
    println(minus) // 4

    val mul = 6 * 2
    println(mul) // 12

    val divide = 6 / 2
    println(divide) // 3

    val divide2 = 7 / 2
    println(divide2) // 3 (!)

    val divide3 = 8 / 2
    println(divide3) // 4

    val divide4 = 7 / 2.0
    println(divide4) // 3.5 (!)

    val remainder = 7 % 2
    println(remainder) // 1
}
